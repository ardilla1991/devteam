package by.htp.devteam.bean;

public class Qualification {
	
	private Long id;
	private String title;
	
	public Qualification() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
}
